public class SumofNumbers {
    public static void main(String[] args) {
        int input = 6;
        int sum=0;

        for(int i=1;i<=input;i++)
        {
            sum=sum+i;
            System.out.println("The sum of "+ input +" is: " + sum );
        }
    }
}
